    private void loadDataPegawai(String keyword) {
        DefaultTableModel model = new DefaultTableModel(new String[]{
            "NIK", "NAMA", "JABATAN", "EMAIL", "NO. TELP", "STATUS", "EDIT", "HAPUS"
        }, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblPegawai.setModel(model);

        try {
            Connection conn = Koneksi.configDB();
            String sql = "SELECT * FROM pegawai WHERE nama_pegawai LIKE ? OR id_pegawai LIKE ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, "%" + keyword + "%");
            ps.setString(2, "%" + keyword + "%");
            
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_pegawai"),
                    rs.getString("nama_pegawai"),
                    rs.getString("id_jabatan"), // Menyesuaikan nama di DB
                    rs.getString("email"),
                    "-", // Diakali karena no_telp belum ada di tabel MySQL
                    "-", // Diakali karena status belum ada di tabel MySQL
                    "[ Edit ]",  // Teks edit akan muncul di setiap baris
                    "[ Hapus ]"  // Teks hapus akan muncul di setiap baris
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error load data: " + e.getMessage());
        }
    }
