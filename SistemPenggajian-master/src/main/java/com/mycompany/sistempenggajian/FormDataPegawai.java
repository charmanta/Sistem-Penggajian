    private void tblPegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPegawaiMouseClicked
       int baris = tblPegawai.getSelectedRow();
        int kolom = tblPegawai.getSelectedColumn(); 
        
        if (baris != -1) {
            String nikTerpilih = tblPegawai.getValueAt(baris, 0).toString();
            String namaTerpilih = tblPegawai.getValueAt(baris, 1).toString();
            
            if (kolom == 6) {
                DialogEditPegawai editDialog = new DialogEditPegawai(this, true, nikTerpilih);
                editDialog.setVisible(true);
                JOptionPane.showMessageDialog(this, "Akan mengedit NIK: " + nikTerpilih);
                
            } else if (kolom == 7) { // Index 7 adalah kolom "[ Hapus ]"
                int konfirmasi = JOptionPane.showConfirmDialog(this, 
                        "Yakin ingin menghapus data " + namaTerpilih + "?", 
                        "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
                        
                if (konfirmasi == JOptionPane.YES_OPTION) {
                    hapusPegawai(nikTerpilih);
                }
            }
        }
    }//GEN-LAST:event_tblPegawaiMouseClicked

