private void loadData(String id) {
       
        try {
            Connection conn = Koneksi.configDB();
            ResultSet res = conn.createStatement().executeQuery("SELECT * FROM pegawai WHERE id_pegawai = '" + id + "'");
            if (res.next()) {
                txtNik.setText(res.getString("id_pegawai"));
                txtNik.setEditable(false);
                txtNamaLengkap.setText(res.getString("nama_pegawai"));
                txtEmail.setText(res.getString("email"));
                txtNoTelp.setText(res.getString("no_hp"));
                txtAlamat.setText(res.getString("alamat"));
                
                // --- INI CARA BENAR MEMILIH JABATAN DI COMBOBOX ---
                String idJabatanPegawai = res.getString("id_jabatan");
                for (int i = 0; i < cbxJabatan.getItemCount(); i++) {
                    String item = cbxJabatan.getItemAt(i);
                    if (item.startsWith(idJabatanPegawai + " - ")) {
                        cbxJabatan.setSelectedIndex(i);
                        break;
                    }
                }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat: " + e.getMessage());
        }
    }
