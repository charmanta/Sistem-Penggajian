    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
                                   
        try {
            // 1. Ambil nilai dari ComboBox
            String jabatanTerpilih = cbxJabatan.getSelectedItem().toString();
            String idJabatan = "";

            // 2. Cek apakah formatnya sudah benar (mengandung tanda " - ")
            if (jabatanTerpilih.contains(" - ")) {
                idJabatan = jabatanTerpilih.split(" - ")[0].trim();
            } else {
                // Jika format salah (misal isinya cuma "Item 1" atau nama jabatan saja)
                JOptionPane.showMessageDialog(this, "ERROR: Format ComboBox salah!\nNilai yang terbaca: '" + jabatanTerpilih + "'");
                return; // Batalkan proses simpan
            }

            // 3. TAMPILKAN POP-UP UNTUK CEK ID JABATAN
            int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Sistem akan menyimpan data ini:\nNIK: " + txtNik.getText() + "\nID Jabatan yang dikirim: '" + idJabatan + "'\n\nApakah ID Jabatan tersebut benar ada di database Anda?", 
                "Cek Data Sebelum Simpan", 
                JOptionPane.YES_NO_OPTION);
                
            if (konfirmasi != JOptionPane.YES_OPTION) {
                return; // Batal simpan jika diklik No
            }

            // 4. Proses Simpan ke Database
            Connection conn = Koneksi.configDB();
            String sql = "UPDATE pegawai SET nama_pegawai=?, email=?, no_hp=?, alamat=?, id_jabatan=? WHERE id_pegawai=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            
            ps.setString(1, txtNamaLengkap.getText());
            ps.setString(2, txtEmail.getText());
            ps.setString(3, txtNoTelp.getText());
            ps.setString(4, txtAlamat.getText());
            ps.setString(5, idJabatan); 
            ps.setString(6, txtNik.getText());
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
            this.dispose();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal update: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void cbxJabatanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxJabatanActionPerformed
        try {
        Connection conn = Koneksi.configDB();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM jabatan");
        cbxJabatan.removeAllItems(); // Bersihkan item lama
        while (rs.next()) {
            // Format: "ID - NamaJabatan"
            cbxJabatan.addItem(rs.getString("id_jabatan") + " - " + rs.getString("nama_jabatan"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load jabatan: " + e.getMessage());
    }
    }//GEN-LAST:event_cbxJabatanActionPerformed
private void isiComboBoxJabatan() {
        try {
            Connection conn = Koneksi.configDB();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM jabatan");
            
            cbxJabatan.removeAllItems(); // Bersihkan "Item 1, Item 2, dll" bawaan Netbeans
            
            while (rs.next()) {
                // Formatkan menjadi "ID - NamaJabatan"
                cbxJabatan.addItem(rs.getString("id_jabatan") + " - " + rs.getString("nama_jabatan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load jabatan: " + e.getMessage());
        }
    }
