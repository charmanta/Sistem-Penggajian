    private void loadJabatan() {
        cmbJabatan.removeAllItems();
        try {
            Connection conn = (Connection) Koneksi.configDB();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery("SELECT id_jabatan, nama_jabatan FROM jabatan");
            while (res.next()) {
                cmbJabatan.addItem(res.getString("id_jabatan") + " - " + res.getString("nama_jabatan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data jabatan: " + e.getMessage());
        }
    }

