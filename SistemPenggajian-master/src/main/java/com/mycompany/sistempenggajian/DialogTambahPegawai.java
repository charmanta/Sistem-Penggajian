    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String nama = jTextField1.getText();
        String nik = jTextField2.getText();
        String email = jTextField3.getText();
        String noTelp = jTextField4.getText();
        String alamat = jTextField7.getText();
        String jenisKelamin = cmbJenisKelamin.getSelectedItem().toString();
        
        String idJabatan = getSelectedId(cmbJabatan);
        
        if (idJabatan == null) {
            JOptionPane.showMessageDialog(this, "Pilih jabatan yang valid!");
            return;
        }

        if (nik.isEmpty() || nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon isi NIK dan Nama!");
            return;
        }

        String sql = "INSERT INTO pegawai (id_pegawai, nama_pegawai, email, no_hp, alamat, jenis_kelamin, id_jabatan, status) VALUES (?, ?, ?, ?, ?, ?, ?, 'Aktif')";

        Connection conn = null;
        PreparedStatement ps = null;

        try {
            conn = Koneksi.configDB();
            ps = conn.prepareStatement(sql);
            ps.setString(1, nik);
            ps.setString(2, nama);
            ps.setString(3, email);
            ps.setString(4, noTelp);
            ps.setString(5, alamat);
            ps.setString(6, jenisKelamin);
            ps.setString(7, idJabatan);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } finally {
            try { if (ps != null) ps.close(); } catch (Exception e) {}
            try { if (conn != null) conn.close(); } catch (Exception e) {}
        }
    }      
       

private String getSelectedId(javax.swing.JComboBox combo) {
    Object selected = combo.getSelectedItem();
    if (selected == null) return null;
    
    String item = selected.toString();
    return item.contains(" - ") ? item.split(" - ")[0].trim() : null;

    }//GEN-LAST:event_btnSimpanActionPerformed

