    private void loadDataKehadiran(int idPegawai) {
        java.time.LocalDate now = java.time.LocalDate.now();
        int bulan = now.getMonthValue();
        int tahun = now.getYear();

        java.time.LocalDate awalBulan = java.time.LocalDate.of(tahun, bulan, 1);
        int totalHariKerja = 0;
        for (java.time.LocalDate d = awalBulan; !d.isAfter(now); d = d.plusDays(1)) {
            java.time.DayOfWeek hari = d.getDayOfWeek();
            if (hari != java.time.DayOfWeek.SATURDAY && hari != java.time.DayOfWeek.SUNDAY) {
                totalHariKerja++;
            }
        }

        String sql = "SELECT "
                + "SUM(CASE WHEN status_presensi IN ('hadir','terlambat') THEN 1 ELSE 0 END) AS hadir, "
                + "SUM(CASE WHEN status_presensi = 'izin' THEN 1 ELSE 0 END) AS izin, "
                + "COUNT(*) AS total_tercatat "
                + "FROM presensi "
                + "WHERE id_pegawai = ? "
                + "AND MONTH(tgl_presensi) = ? AND YEAR(tgl_presensi) = ?";

        java.sql.Connection conn = DBConnection.getKoneksi();
        try (java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, idPegawai);
            ps.setInt(2, bulan);
            ps.setInt(3, tahun);
            java.sql.ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                int hadir = rs.getInt("hadir");
                int izin = rs.getInt("izin");
                int totalTercatat = rs.getInt("total_tercatat");
                int alpha = Math.max(0, totalHariKerja - totalTercatat);

                lblHadir.setText(String.valueOf(hadir));
                lblIzin.setText(String.valueOf(izin));
                lblSakit.setText("0");
                lblAlpha.setText(String.valueOf(alpha));
                lblRingkasanHadir.setText(hadir + "/" + totalHariKerja + " hari hadir");
            }

        } catch (Exception e) {
            e.printStackTrace();
            lblHadir.setText("0");
            lblIzin.setText("0");
            lblAlpha.setText("0");
            lblRingkasanHadir.setText("0/" + totalHariKerja + " hari hadir");
        }
    }

    private void loadDataGaji(int idPegawai) {
        java.time.LocalDate now = java.time.LocalDate.now();
        String periodeBulanIni = now.getYear() + "-" + String.format("%02d", now.getMonthValue());

        String sql = "SELECT pg.tunjangan, pg.potongan, pg.total_gaji, pg.periode, "
                + "j.gaji_pokok "
                + "FROM penggajian pg "
                + "JOIN pegawai p ON pg.id_penggajian = p.id_pegawai "
                + "JOIN jabatan j ON p.id_jabatan = j.id_jabatan "
                + "WHERE p.id_pegawai = ? AND pg.periode = ? "
                + "LIMIT 1";

        java.sql.Connection conn = DBConnection.getKoneksi();
        try (java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, idPegawai);
            ps.setString(2, periodeBulanIni);
            java.sql.ResultSet rs = ps.executeQuery();

            java.text.NumberFormat fmt = new java.text.DecimalFormat("#,###");

            if (rs.next()) {
                double gajiPokok = rs.getDouble("gaji_pokok");
                double tunjangan = rs.getDouble("tunjangan");
                double potongan = rs.getDouble("potongan");
                double totalGaji = rs.getDouble("total_gaji");

                String periode = rs.getString("periode");
                String[] pecah = periode.split("-");
                String[] namaBulan = {"", "Januari", "Februari", "Maret", "April", "Mei", "Juni",
                    "Juli", "Agustus", "September", "Oktober", "November", "Desember"};
                int bln = Integer.parseInt(pecah[1]);
                lblPeriode.setText("Periode: " + namaBulan[bln] + " " + pecah[0]);

                lblGajiPokok.setText("Rp " + fmt.format(gajiPokok));
                lblTunjangan.setText("+ Rp " + fmt.format(tunjangan));
                lblPotongan.setText("- Rp " + fmt.format(potongan));
                lblTotalGaji.setText("Rp " + fmt.format(totalGaji));

            } else {
                String sqlGajiPokok = "SELECT j.gaji_pokok FROM pegawai p "
                        + "JOIN jabatan j ON p.id_jabatan = j.id_jabatan "
                        + "WHERE p.id_pegawai = ?";
                try (java.sql.PreparedStatement ps2 = conn.prepareStatement(sqlGajiPokok)) {
                    ps2.setInt(1, idPegawai);
                    java.sql.ResultSet rs2 = ps2.executeQuery();
                    if (rs2.next()) {
                        double gajiPokok = rs2.getDouble("gaji_pokok");
                        lblGajiPokok.setText("Rp " + fmt.format(gajiPokok));
                    }
                }
                lblTunjangan.setText("+ Rp 0");
                lblPotongan.setText("- Rp 0");
                lblTotalGaji.setText("Belum diproses");
                lblPeriode.setText("Periode: " + periodeBulanIni);
            }

        } catch (Exception e) {
            e.printStackTrace();
            lblTotalGaji.setText("Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnKeluarActionPerformed

