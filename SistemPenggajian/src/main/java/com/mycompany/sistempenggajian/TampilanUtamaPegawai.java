    private String getNIK(int idPegawai) {
        String nik = "";
        String sql = "SELECT nik FROM pegawai WHERE id_pegawai = ?";
        java.sql.Connection conn = DBConnection.getKoneksi();
        try (java.sql.PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setInt(1, idPegawai);
            java.sql.ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                nik = rs.getString("nik");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return nik;
    }

