    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        Connection con = DBConnection.getKoneksi();

        String username = input_username.getText();
        String password = new String(input_password.getPassword());

        // TAMBAHKAN INI
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password wajib diisi!");
            return;
        }

        String loginSql
                = "SELECT u.*, p.id_pegawai, p.nama, p.email, p.no_tlpn, p.alamat, j.nama_jabatan "
                + "FROM user u "
                + "JOIN pegawai p ON u.id_user = p.id_user "
                + "JOIN jabatan j ON p.id_jabatan = j.id_jabatan "
                + "WHERE u.username=? AND u.password=?";

        try {
            PreparedStatement ps = con.prepareStatement(loginSql);
            ps.setString(1, username);
            ps.setString(2, password);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                String hakAkses = rs.getString("hak_akses");
                String nama = rs.getString("nama");
                String email = rs.getString("email");
                String jabatan = rs.getString("nama_jabatan");
                String telepon = rs.getString("no_tlpn");
                String alamat = rs.getString("alamat");
                int idPegawai = rs.getInt("id_pegawai");

// buat inisial otomatis dari nama, contoh "Ahmad Santoso" -> "AS"
                String[] kataNama = nama.trim().split(" ");
                String inisial = "";
                for (String kata : kataNama) {
                    if (!kata.isEmpty()) {
                        inisial += kata.charAt(0);
                    }
                }
                inisial = inisial.toUpperCase();

                JOptionPane.showMessageDialog(this, "Login berhasil sebagai " + hakAkses);
                TampilanUtamaPegawai obj = new TampilanUtamaPegawai(nama, jabatan, email, telepon, alamat, inisial, idPegawai);
                obj.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Username atau Password salah!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error : " + e.getMessage());
        }
    }//GEN-LAST:event_btn_loginActionPerformed

